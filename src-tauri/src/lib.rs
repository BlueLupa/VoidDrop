#[tauri::command]
fn plot_all(state: State<'_, AppState>) -> Result<Vec<Star>, String> {
    let db = state.db.lock().map_err(|e| e.to_string())?;

    let mut sql_query = db
        .prepare(
            "
    SELECT e.id, e.content, e.type, e.created, 1.0-vec_distance_cosine(emb.embedding, ?1), 1.0-vec_distance_cosine(emb.embedding, ?2)
    FROM echoes e
    JOIN embeddings emb ON e.id = emb.note_id
    ",
        )
        .map_err(|e| e.to_string())?;

    let vec_a_bytes = bytemuck::cast_slice(&VEC_A);
    let vec_b_bytes = bytemuck::cast_slice(&VEC_B);

    let mut out: Vec<Star> = sql_query
        .query_map(params![vec_a_bytes, vec_b_bytes], |f| {
            let id: i64 = f.get(0)?;
            let content: String = f.get(1)?;
            let file_type: u32 = f.get(2)?;
            let time_stamp: String = f.get(3)?;
            let x: f32 = f.get(4)?;
            let y: f32 = f.get(5)?;

            Ok(Star {
                x,
                y,
                frag: ResultEcho {
                    id,
                    content,
                    file_type,
                    similarity: 0.0,
                    time_stamp,
                },
            })
        })
        .map_err(|e| e.to_string())?
        .into_iter()
        .map(|f| f.unwrap())
        .collect();

    let mut sql_query = db
        .prepare(
            "
    SELECT e.id, e.content, e.type, e.created, 1.0-vec_distance_cosine(img.embedding, ?1), 1.0-vec_distance_cosine(img.embedding, ?2)
         FROM echoes e
         JOIN img_emb img ON e.id = img.note_id
    ",
        )
        .map_err(|e| e.to_string())?;

    let vec_a_bytes = bytemuck::cast_slice(&VEC_A_IMG);
    let vec_b_bytes = bytemuck::cast_slice(&VEC_B_IMG);

    let imgs: Vec<Star> = sql_query
        .query_map(params![vec_a_bytes, vec_b_bytes], |f| {
            let id: i64 = f.get(0)?;
            let content: String = f.get(1)?;
            let file_type: u32 = f.get(2)?;
            let time_stamp: String = f.get(3)?;
            let x: f32 = f.get(4)?;
            let y: f32 = f.get(5)?;

            Ok(Star {
                x,
                y,
                frag: ResultEcho {
                    id,
                    content,
                    file_type,
                    similarity: 0.0,
                    time_stamp,
                },
            })
        })
        .map_err(|e| e.to_string())?
        .into_iter()
        .map(|f| f.unwrap())
        .collect();

    out.extend(imgs);

    let min_x = out.iter().map(|s| s.x).fold(f32::MAX, f32::min);
    let max_x = out.iter().map(|s| s.x).fold(f32::MIN, f32::max);
    let min_y = out.iter().map(|s| s.y).fold(f32::MAX, f32::min);
    let max_y = out.iter().map(|s| s.y).fold(f32::MIN, f32::max);

    for star in &mut out {
        star.x = (((star.x - min_x) / (max_x - min_x)) * 2.0 - 1.0).clamp(-0.95, 0.95);
        star.y = (((star.y - min_y) / (max_y - min_y)) * 2.0 - 1.0).clamp(-0.95, 0.95);
    }
    Ok(out)
}

#[tauri::command]
fn delete_id(id: u64, state: State<'_, AppState>) -> Result<(), String> {
    let mut db = state.db.lock().map_err(|e| e.to_string())?;

    let (typ, filename): (u32, String) = db
        .query_row(
            "SELECT type, content FROM echoes WHERE id = ?1",
            [id as i64],
            |row| Ok((row.get(0)?, row.get(1)?)),
        )
        .map_err(|e| e.to_string())?;

    if typ != 0 {
        let mut path = PathBuf::from(&state.file_dir);
        path.push("files");
        path.push(filename);

        let _ = std::fs::remove_file(path);
    }

    let tx = db.transaction().map_err(|e| e.to_string())?;

    tx.execute("DELETE FROM echoes WHERE id = ?1", [id as i64])
        .map_err(|e| e.to_string())?;

    tx.execute("DELETE FROM embeddings WHERE note_id = ?1", [id as i64])
        .map_err(|e| e.to_string())?;

    tx.execute("DELETE FROM img_emb WHERE note_id = ?1", [id as i64])
        .map_err(|e| e.to_string())?;

    tx.commit().map_err(|e| e.to_string())?;

    Ok(())
}

fn init_db(dbp: PathBuf) -> Result<Connection> {
    unsafe {
        sqlite3_auto_extension(Some(std::mem::transmute(sqlite3_vec_init as *const ())));
    }

    let db = Connection::open(dbp)?;

    db.execute(
        "CREATE TABLE IF NOT EXISTS echoes (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            content TEXT NOT NULL,
            type INTEGER,
            created DATETIME DEFAULT CURRENT_TIMESTAMP
        );",
        [],
    )?;

    db.execute(
        "CREATE VIRTUAL TABLE IF NOT EXISTS embeddings USING vec0(
            note_id INTEGER PRIMARY KEY,
            embedding float[384]
        );",
        [],
    )?;

    db.execute(
        "CREATE VIRTUAL TABLE IF NOT EXISTS img_emb USING vec0(
            note_id INTEGER PRIMARY KEY,
            embedding float[512]
        );",
        [],
    )?;

    Ok(db)
}

fn readable(bytes: &[u8]) -> bool {
    let limit = std::cmp::min(bytes.len(), 1000);
    let sample = &bytes[..limit];

    if sample.contains(&0) {
        return false;
    }

    match std::str::from_utf8(sample) {
        Ok(_) => true,
        Err(e) => e.error_len().is_none(),
    }
}

fn process_text(x: &str, name: &str, state: &State<'_, AppState>) -> Result<Vec<f32>, String> {
    if x.is_empty() {
        return Err("Text is empty".to_string());
    }
    let mut locked_model = state.model.lock().map_err(|e| e.to_string());
    let model = locked_model.as_mut().unwrap().as_mut().unwrap();

    let mut embeddings: Vec<f64> = Vec::with_capacity(384);

    let mut chunks_n = 0;
    for chunk in &x.chars().chunks(1000) {
        let st: String = chunk.collect();
        let st = format!("{}\n\n{}", name, st);
        let model_out = model.embed(vec![st], None).map_err(|e| e.to_string())?;

        if embeddings.is_empty() {
            embeddings = model_out[0].iter().map(|&v| v as f64).collect();
        } else {
            for (emb, val) in embeddings.iter_mut().zip(model_out[0].iter()) {
                *emb += *val as f64;
            }
        }
        chunks_n += 1;
    }

    let mut out: Vec<f32> = embeddings
        .into_iter()
        .map(|e| (e / chunks_n as f64) as f32)
        .collect();
    let magnitude: f32 = out.iter().map(|v| v * v).sum::<f32>().sqrt();
    if magnitude > 0.0 {
        for v in &mut out {
            *v /= magnitude;
        }
    }

    Ok(out)
}

#[tauri::command]
fn process_file(bytes: Vec<u8>, file_name: &str, state: State<'_, AppState>) -> Result<(), String> {
    let file_timestamp = Local::now().format("%Y%m%d_%H%M%S").to_string();
    let mut file_path = PathBuf::from(state.file_dir.clone());
    file_path.push("files");
    let x = file_timestamp + "_" + file_name;
    file_path.push(&x);

    let mut ext = "txt".to_string();
    if let Some(a) = file_path.extension() {
        if let Some(b) = a.to_str() {
            ext = b.to_lowercase();
        }
    }

    match ext.as_str() {
        "png" | "jpg" | "jpeg" | "webp" => {
            std::fs::write(&file_path, &bytes).map_err(|e| e.to_string())?;

            let mut locked_model = state.img_model.lock().map_err(|e| e.to_string());
            let img_model = locked_model.as_mut().unwrap().as_mut().unwrap();
            let img = image::load_from_memory(&bytes).map_err(|e| e.to_string())?;
            let e_vec = img_model
                .embed_images(vec![img])
                .map_err(|e| e.to_string())?;

            let emb = &e_vec[0];

            let mut db = state.db.lock().map_err(|e| e.to_string())?;
            let tx = db.transaction().map_err(|e| e.to_string())?;

            tx.execute(
                "INSERT INTO echoes (content, type) VALUES (?1, ?2)",
                params![&x, 2],
            )
            .map_err(|e| e.to_string())?;

            let id = tx.last_insert_rowid();

            let emb_bytes: Vec<u8> = emb.iter().flat_map(|f| f.to_le_bytes()).collect();
            tx.execute(
                "INSERT INTO img_emb (note_id, embedding) VALUES (?1, ?2)",
                params![id, emb_bytes],
            )
            .map_err(|e| e.to_string())?;

            tx.commit().map_err(|e| e.to_string())?;
        }

        "docx" | "pptx" | "xlsx" | "pdf" => {
            std::fs::write(&file_path, &bytes).map_err(|e| e.to_string())?;

            let mut text: String = String::new();
            if ext == "pdf" {
                text = pdf_extract::extract_text_from_mem(&bytes).map_err(|e| e.to_string())?;
            } else {
                match ext.as_str() {
                    "docx" => {
                        let mut doc = dotext::Docx::open(file_path).map_err(|e| e.to_string())?;
                        doc.read_to_string(&mut text).map_err(|e| e.to_string())?;
                    }
                    "pptx" => {
                        let mut ppt = dotext::Pptx::open(file_path).map_err(|e| e.to_string())?;
                        ppt.read_to_string(&mut text).map_err(|e| e.to_string())?;
                    }
                    "xlsx" => {
                        let mut xls = dotext::Xlsx::open(file_path).map_err(|e| e.to_string())?;
                        xls.read_to_string(&mut text).map_err(|e| e.to_string())?;
                    }
                    _ => unreachable!(),
                }
            }
            let emb_out = process_text(&text, &format!("{} document: {}", ext, file_name), &state)?;

            let mut db = state.db.lock().map_err(|e| e.to_string())?;
            let tx = db.transaction().map_err(|e| e.to_string())?;

            tx.execute(
                "INSERT INTO echoes (content, type) VALUES (?1, ?2)",
                params![&x, 1],
            )
            .map_err(|e| e.to_string())?;

            let id = tx.last_insert_rowid();

            let emb_bytes: Vec<u8> = emb_out.iter().flat_map(|f| f.to_le_bytes()).collect();
            tx.execute(
                "INSERT INTO embeddings (note_id, embedding) VALUES (?1, ?2)",
                params![id, emb_bytes],
            )
            .map_err(|e| e.to_string())?;

            tx.commit().map_err(|e| e.to_string())?;
        }

        _ => {
            if readable(&bytes) {
                let text = std::str::from_utf8(&bytes).map_err(|e| e.to_string())?;
                std::fs::write(&file_path, &bytes).map_err(|e| e.to_string())?;

                let expanded_type = match ext.as_str() {
                    // C++ family
                    "c" => "C source code",
                    "h" => "C header",
                    "cpp" | "cc" | "cxx" => "C++ source code",
                    "hpp" | "hxx" => "C++ header",

                    // Code
                    "rs" => "Rust code",
                    "py" => "Python code",
                    "go" => "Go code",
                    "java" => "Java code",
                    "cs" => "C# code",
                    "rb" => "Ruby code",
                    "php" => "PHP code",
                    "sh" | "bash" | "zsh" => "Shell script",
                    "lua" => "Lua script",

                    // Frontend code
                    "js" | "cjs" | "mjs" => "JavaScript code",
                    "jsx" => "React JavaScript code",
                    "ts" => "TypeScript code",
                    "tsx" => "React TypeScript code",
                    "swift" => "Swift code",
                    "kt" | "kts" => "Kotlin code",
                    "dart" => "Dart code",

                    // Frontend
                    "html" | "htm" => "HTML code",
                    "css" => "CSS code",
                    "scss" | "sass" => "SCSS stylesheet",

                    // Data
                    "sql" => "SQL database query",
                    "json" => "JSON data",
                    "yaml" | "yml" => "YAML configuration",
                    "toml" => "TOML configuration",
                    "xml" => "XML data",
                    "csv" => "CSV data",

                    // Documents
                    "md" => "Markdown document",
                    "txt" => "Text document",

                    // idk what this is
                    _ => "File",
                };

                let emb_out =
                    process_text(&text, &format!("{}: {}", expanded_type, file_name), &state)?;

                let mut db = state.db.lock().map_err(|e| e.to_string())?;
                let tx = db.transaction().map_err(|e| e.to_string())?;

                tx.execute(
                    "INSERT INTO echoes (content, type) VALUES (?1, ?2)",
                    params![&x, 1],
                )
                .map_err(|e| e.to_string())?;

                let id = tx.last_insert_rowid();

                let emb_bytes: Vec<u8> = emb_out.iter().flat_map(|f| f.to_le_bytes()).collect();
                tx.execute(
                    "INSERT INTO embeddings (note_id, embedding) VALUES (?1, ?2)",
                    params![id, emb_bytes],
                )
                .map_err(|e| e.to_string())?;

                tx.commit().map_err(|e| e.to_string())?;
            } else {
                return Err("Unsupported file format".to_string());
            }
        }
    }
    Ok(())
}


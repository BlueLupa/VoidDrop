#[tauri::command]
fn process_new_input(x: &str, state: State<'_, AppState>) -> Result<(), String> {
    let emb_out = process_text(x, "Note", &state)?;

    let mut db = state.db.lock().map_err(|e| e.to_string())?;
    let tx = db.transaction().map_err(|e| e.to_string())?;

    tx.execute(
        "INSERT INTO echoes (content, type) VALUES (?1, ?2)",
        params![&x, 0],
    )
    .map_err(|e| e.to_string())?;

    let id = tx.last_insert_rowid();

    let emb_bytes: Vec<u8> = emb_out.iter().flat_map(|f| f.to_le_bytes()).collect();
    tx.execute(
        "INSERT INTO embeddings (note_id, embedding) VALUES (?1, ?2)",
        params![id, emb_bytes],
    )
    .map_err(|e| e.to_string())?;

    tx.commit().map_err(|e| e.to_string())?;
    Ok(())
}

#[tauri::command]
fn sql_n(state: State<'_, AppState>) -> Result<i64, String> {
    let db = state.db.lock().map_err(|e| e.to_string())?;
    let count: i64 = db
        .query_row("SELECT COUNT(*) FROM echoes", [], |row| row.get(0))
        .map_err(|e| e.to_string())?;

    Ok(count)
}

#[derive(Debug, serde::Serialize)]
struct ResultEcho {
    id: i64,
    content: String,
    similarity: f32,
    file_type: u32,
    time_stamp: String,
}

#[tauri::command]
fn search(query: &str, state: State<'_, AppState>) -> Result<Vec<ResultEcho>, String> {
    let db = state.db.lock().map_err(|e| e.to_string())?;
    let mut locked_model = state.model.lock().map_err(|e| e.to_string())?;
    let model = locked_model
        .as_mut()
        .ok_or("Models not initialised yet. Please wait...")?;

    let mut locked_img_model = state.img_search_model.lock().map_err(|e| e.to_string())?;
    let img_search_model = locked_img_model
        .as_mut()
        .ok_or("Models not initialised yet. Please wait...")?;

    let e = model.embed(vec![query], None).map_err(|e| e.to_string())?;
    let img_e = img_search_model
        .embed(vec![query], None)
        .map_err(|e| e.to_string())?;
    let query_embed = &e[0];
    let img_query_embed = &img_e[0];

    let query_bytes: Vec<u8> = query_embed.iter().flat_map(|f| f.to_le_bytes()).collect();
    let img_query_bytes: Vec<u8> = img_query_embed
        .iter()
        .flat_map(|f| f.to_le_bytes())
        .collect();

    let mut sql_query = db
        .prepare(
            "
    SELECT e.id, e.content, e.type, e.created, vec_distance_cosine(emb.embedding, ?1) AS dist
    FROM echoes e
    JOIN embeddings emb ON e.id = emb.note_id
    ORDER BY dist ASC
    ",
        )
        .map_err(|e| e.to_string())?;

    let mut out: Vec<ResultEcho> = sql_query
        .query_map(params![query_bytes], |f| {
            let id: i64 = f.get(0)?;
            let content: String = f.get(1)?;
            let file_type: u32 = f.get(2)?;
            let time_stamp: String = f.get(3)?;
            let dist: f32 = f.get(4)?;

            Ok(ResultEcho {
                id,
                content,
                similarity: (1.0 - dist),
                file_type,
                time_stamp,
            })
        })
        .map_err(|e| e.to_string())?
        .into_iter()
        .map(|f| f.unwrap())
        .collect();

    let mut img_query = db.prepare(
        "SELECT e.id, e.content, e.type, e.created, vec_distance_cosine(img.embedding, ?1) AS dist
         FROM echoes e
         JOIN img_emb img ON e.id = img.note_id
         ORDER BY dist ASC"
    ).map_err(|e| e.to_string())?;

    let img_out: Vec<ResultEcho> = img_query
        .query_map(params![img_query_bytes], |f| {
            let k: f32 = 30.0; // inflation
            let x0: f32 = 0.22; // Midpoint

            let raw_similarity = 1.0 - f.get::<_, f32>(4)?;
            let inflated_score = 1.0 / (1.0 + (-k * (raw_similarity - x0)).exp());

            // Note: Searching Images via text naturally gives lower a similarity
            // A score of 15% is noise, while 35% is a strong match
            // Bc of this the image results have to be inflated so the percentages and rankings are calibrated with the text results
            Ok(ResultEcho {
                id: f.get(0)?,
                content: f.get(1)?,
                file_type: f.get(2)?,
                time_stamp: f.get(3)?,
                similarity: inflated_score,
            })
        })
        .map_err(|e| e.to_string())?
        .filter_map(Result::ok)
        .collect();

    out.extend(img_out);
    out.sort_by(|a, b| {
        b.similarity
            .partial_cmp(&a.similarity)
            .unwrap_or(std::cmp::Ordering::Equal)
    });
    Ok(out)
}

#[tauri::command]
fn fetch_all(state: State<'_, AppState>) -> Result<Vec<ResultEcho>, String> {
    let db = state.db.lock().map_err(|e| e.to_string())?;
    let mut sql_query = db
        .prepare(
            "
    SELECT e.id, e.content, e.type, e.created AS date
    FROM echoes e
    ORDER BY date DESC
    ",
        )
        .map_err(|e| e.to_string())?;

    let out: Result<Vec<ResultEcho>, String> = sql_query
        .query_map([], |f| {
            let id: i64 = f.get(0)?;
            let content: String = f.get(1)?;
            let file_type: u32 = f.get(2)?;
            let time_stamp: String = f.get(3)?;

            Ok(ResultEcho {
                id,
                content,
                similarity: 0.0,
                file_type,
                time_stamp,
            })
        })
        .map_err(|e| e.to_string())?
        .into_iter()
        .map(|x| x.map_err(|e| e.to_string()))
        .collect();

    out
}

